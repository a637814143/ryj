-- 查询验证数据
SELECT 
    jp.id,
    jp.title,
    e.company_name,
    jp.work_type,
    jp.status,
    jp.location,
    jp.salary_range,
    COUNT(jr.requirement) as requirement_count
FROM job_posting jp
LEFT JOIN employer e ON jp.employer_id = e.id
LEFT JOIN job_requirement jr ON jp.id = jr.job_id
GROUP BY jp.id
ORDER BY jp.id;

-- 统计信息
SELECT 
    '企业总数' as item,
    COUNT(*) as count
FROM employer
UNION ALL
SELECT 
    '职位总数' as item,
    COUNT(*) as count
FROM job_posting
UNION ALL
SELECT 
    '开放职位数' as item,
    COUNT(*) as count
FROM job_posting
WHERE status = 'OPEN';

